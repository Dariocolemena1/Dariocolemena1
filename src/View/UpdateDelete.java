    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int checkId = 0;
        String id = IdMembro.getText();
        try {
            Connection con = ConnectionProvider.getCon();
            java.sql.Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT * FROM membro where id='" + id + "'");
            while (rs.next()) {

                checkId = 1;
                IdMembro.setEditable(false);
                Nome.setText(rs.getString(2));
                Telefone.setText(rs.getString(3));
                Email.setText(rs.getString(4));
                Genero.setEditable(false);
                Genero.setText(rs.getString(5));
                NomePai.setText(rs.getString(6));
                NomeMae.setText(rs.getString(7));
                Idade.setText(rs.getString(10));
                Time.setText(rs.getString(8));
                Time.setEditable(false);
                Nif.setText(rs.getString(9));
                Pagamento.setText(rs.getString(11));

            }
            if (checkId == 0) {
                JOptionPane.showMessageDialog(null, "ID Não existe");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

