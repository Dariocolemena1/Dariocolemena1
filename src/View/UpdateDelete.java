    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       String id = IdMembro.getText();
       String nome = Nome.getText();
       String telefone = Telefone.getText();
       String email = Email.getText();
       String genero = Genero.getText();
       String nomePai = NomePai.getText();
       String nomeMae = NomeMae.getText();
       String idade = Idade.getText();
       String time = Time.getText();
       String nif = Nif.getText();
       String pagamento = Pagamento.getText();
       try {
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement("UPDATE membro set nome=?, telefone=?, email=?, nomePai=?, nomeMae=?,nif=?, idade=?, pagamento=? where id=?");
            
            ps.setString(1, nome);
            ps.setString(2, telefone);
            ps.setString(3, email);
            ps.setString(4, nomePai);
            ps.setString(5, nomeMae);
            ps.setString(6, nif);
            ps.setString(7, idade);
            ps.setString(8, pagamento);
            ps.setString(9, id);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Atualizado com Sucesso");
            setVisible(false);
            new UpdateDelete().setVisible(true);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

